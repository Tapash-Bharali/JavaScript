// Primitive Data Types in JavaScript
// number, string, boolean, null, undefined, symbol, bigint


// number

let a = 10;    
let b = 2.36
console.log(a,b);


// string

let c = "Tapash";
let d = "Bharali";
console.log(c,d);


// Boolean

let login = true;
let f = false;
console.log(login,f);


// Undefined 

let user;
console.log(user);


// Null

let weather = null;
console.log(weather);


// Big int 

let bigInt = 1234567890123456789012345678901234567890n;
console.log(bigInt);


// symbol 

const id1 = Symbol("id");
console.log(id1);



































