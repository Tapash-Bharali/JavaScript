// arrow function

const addNumber = (num1, num2)=>{
  return num1+num2;
}

console.log(addNumber(7,9));


const add_Number = (num1,num2)=> num1+num2;

console.log(add_Number(10,17));




const addingNumber = (num1,num2) => num1 + num2;

const squareNumber = (num) => num*num;

console.log(addingNumber(5,8));
console.log(squareNumber(9));


// If we have single parameter then no need of this ()
const my_SquaredNumber = num => num*num;

console.log(my_SquaredNumber(6));




